  <h4>Visi Misi</h4>
<br>

Surat SDEOPS POLRI Nomor : B / 581 / III / 2010 tanggal 24 Maret
2010 tentang keputusan Kapolri tentang Visi & Misi fungsi Bimmas
Polri untuk dipedomani oleh seluruh anggota Polri khusunya
pengemban fungsi Bimmas. <br><br>

<h5>Visi</h5><br>
Menjadi sahabat dan mitra masyarakat dalam memecahkan masalah
- masalah sosial yang dapat mengganggu ketertiban dan keamanan
lingkungan.
<br><br>
<h5>Misi</h5>
<ol type="a" style="margin-left: 20px">
	<li> Hadir ditengah - tengah masyarakat untuk melindungi,
mengayomi dan melayani masyarakat.</li>
	<li>Membangun komunikasi yang efektif dan intensif dengan
masyarakat baik individu maupun kelompok / komunitas.</li>
	<li>Mengidentifikasi masalah - masalah sosial dan keamanan yang
timbul dalam masyarakat serta menemukan jalan pemecahannya.</li>
	<li> Bersama masyarakat mencegah dan menangkal timbulnya
penyakit masyarakat.</li>
	<li>Bersama masyarakat menjaga ketertiban dan keamanan
lingkungan.</li>
	<li>Membangun dan mengembangkan kemitraan dengan segenap
komunitas dalam memelihara situasi kamtibmas yang kodusif.</li>
	<li> Mendorong partisipasi masyarakat dalam menumbuh
kembangkan daya cegah dan daya tangkal terhadap segala bentuk
gangguan kamtibmas.</li>
</ol>
